
my_list=["Soap","Oil","Maggie","Tea",345,"Buiscuits","rice","Dalia"]

#list element is recognized by its index value
#first element my_list[0]

print(my_list[0])

#there is concept of negative indexing
print(my_list[-1])


#slicing

print(my_list[1:4]) #printing names in list which are between 1 and 4

print(my_list[:]) #as there is no numbers it will print whole list

print(my_list[2:]) # it will print from 3rd name

print(my_list[:5]) #it will print from 1st to 5th

print(my_list[::-1]) #it will print the list form backwards




