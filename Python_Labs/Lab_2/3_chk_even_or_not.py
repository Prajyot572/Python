#WAP to check whether the number is even or not.


#Taking a input number
number=int (input("Enter a number: "))


#Checking the Criteria
#If number are divisible by 2 then even
if number%2==0:
    print(number,"is a even number!")


#If number is not divisible by 2 then odd
else:
    print(number,"is not a even number!")
    
